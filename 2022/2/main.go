func process_strategy(strategy string) int {
	lines := strings.Split(strategy, "\n")
	points := 0
	for _, line := range lines {
		if line == "" {
			continue
		}
		choice := strings.Split(line, " ")
		result := wins[choice[0]][choice[1]]
		points += result
		points += choices[choice[1]]
	}
	return points
}

func process_correct_strategy(strategy string) int {
	lines := strings.Split(strategy, "\n")
	points := 0
	for _, line := range lines {
		if line == "" {
			continue
		}
		choice := strings.Split(line, " ")
		points += correctWins[choice[0]][choice[1]]
	}
	return points
}

